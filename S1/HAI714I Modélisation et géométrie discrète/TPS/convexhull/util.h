struct Basis {
    inline Basis ( Vec3 const & i_origin,  Vec3 const & i_i, Vec3 const & i_j, Vec3 const & i_k) {
        origin = i_origin; i = i_i ; j = i_j ; k = i_k;
    }

    inline Basis ( ) {
        origin = Vec3(0., 0., 0.);
        i = Vec3(1., 0., 0.) ; j = Vec3(0., 1., 0.) ; k = Vec3(0., 0., 1.);
    }
    Vec3 operator [] (unsigned int ib) {
        if(ib==0) return i;
        if(ib==1) return j;
        if(ib==2) return k;
        else return origin;}

    void setOrigin(Vec3 _origin){
        origin = _origin;
        }
    void setI(Vec3 _i){
        i = _i;
        }
    void setJ(Vec3 _j){
        j = _j;
        }

    Vec3 origin;
    Vec3 i;
    Vec3 j;
    Vec3 k;
};
// ------------------------------------
// Application initialization
